import { useState } from 'react';
import { Navigate } from 'react-router-dom';
import { useAuth } from '../context/AuthContext';
import { Button, Input } from '../components/ui';
import { PrivacyPolicyModal } from '../components/features/PrivacyPolicyModal';
import { TermsOfServiceModal } from '../components/features/TermsOfServiceModal';
import { Mail, ArrowLeft } from 'lucide-react';
import { cn } from '../lib/utils';

type AuthMode = 'welcome' | 'email-signup' | 'email-signin' | 'forgot';

export function Login() {
  const { user, loading, signIn, signUp, signInWithGoogle, resetPassword } = useAuth();
  const [mode, setMode] = useState<AuthMode>('welcome');
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [showPrivacyModal, setShowPrivacyModal] = useState(false);
  const [showTermsModal, setShowTermsModal] = useState(false);
  const [isSubmitting, setIsSubmitting] = useState(false);
  const [isOAuthLoading, setIsOAuthLoading] = useState(false);
  const [message, setMessage] = useState<{ type: 'success' | 'error'; text: string } | null>(null);

  // Redirect if already logged in
  if (!loading && user) {
    return <Navigate to="/lists" replace />;
  }

  const resetForm = () => {
    setEmail('');
    setPassword('');
    setMessage(null);
  };

  const handleModeChange = (newMode: AuthMode) => {
    setMode(newMode);
    resetForm();
  };

  const handleGoogleSignIn = async () => {
    setIsOAuthLoading(true);
    setMessage(null);

    const result = await signInWithGoogle();

    if (result?.error) {
      const errorMessage = result.error.message.includes('popup')
        ? 'Please allow popups for this site to sign in with Google'
        : result.error.message.includes('not enabled')
        ? 'Google sign-in is currently unavailable. Please try email sign-in.'
        : result.error.message;

      setMessage({ type: 'error', text: errorMessage });
      setIsOAuthLoading(false);
    }
  };

  const handleEmailSignUp = async (e: React.FormEvent) => {
    e.preventDefault();
    setIsSubmitting(true);
    setMessage(null);

    if (password.length < 6) {
      setMessage({ type: 'error', text: 'Password must be at least 6 characters' });
      setIsSubmitting(false);
      return;
    }

    const result = await signUp(email, password);

    if (result.error) {
      setMessage({ type: 'error', text: result.error.message });
    } else {
      setMessage({
        type: 'success',
        text: 'Account created! Check your email to confirm your account.',
      });
      resetForm();
    }

    setIsSubmitting(false);
  };

  const handleEmailSignIn = async (e: React.FormEvent) => {
    e.preventDefault();
    setIsSubmitting(true);
    setMessage(null);

    const result = await signIn(email, password);

    if (result?.error) {
      setMessage({ type: 'error', text: result.error.message });
    }

    setIsSubmitting(false);
  };

  const handleForgotPassword = async (e: React.FormEvent) => {
    e.preventDefault();
    setIsSubmitting(true);
    setMessage(null);

    const result = await resetPassword(email);

    if (result?.error) {
      setMessage({ type: 'error', text: result.error.message });
    } else {
      setMessage({
        type: 'success',
        text: 'Check your email for the password reset link.',
      });
    }

    setIsSubmitting(false);
  };

  if (loading) {
    return (
      <div className="flex items-center justify-center min-h-[80vh]">
        <div className="animate-spin h-8 w-8 border-4 border-white border-t-transparent rounded-full" />
      </div>
    );
  }

  return (
    <>
      <div className="flex flex-col items-center justify-center min-h-[80vh] max-w-md mx-auto px-6">

        {/* Welcome Screen */}
        {mode === 'welcome' && (
          <>
            <h1 className="text-5xl font-bold text-white mb-12 text-center">
              Welcome to Guet Parat
            </h1>

            <div className="w-full space-y-4 mb-8">
              {/* Google Sign Up Button */}
              <button
                type="button"
                onClick={handleGoogleSignIn}
                disabled={isOAuthLoading}
                className={cn(
                  'w-full flex items-center justify-center gap-3 px-6 py-3',
                  'bg-white hover:bg-gray-50 text-gray-700 font-medium rounded-full',
                  'transition-all duration-200 focus:outline-none focus:ring-2 focus:ring-blue-500',
                  'border border-gray-300 shadow-sm',
                  isOAuthLoading && 'opacity-50 cursor-not-allowed'
                )}
              >
                {isOAuthLoading ? (
                  <>
                    <svg className="animate-spin h-5 w-5 text-gray-600" viewBox="0 0 24 24">
                      <circle
                        className="opacity-25"
                        cx="12"
                        cy="12"
                        r="10"
                        stroke="currentColor"
                        strokeWidth="4"
                        fill="none"
                      />
                      <path
                        className="opacity-75"
                        fill="currentColor"
                        d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"
                      />
                    </svg>
                    <span>Connecting...</span>
                  </>
                ) : (
                  <>
                    <svg className="w-5 h-5" viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg">
                      <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4"/>
                      <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/>
                      <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05"/>
                      <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/>
                    </svg>
                    <span className="text-[15px]">Sign up with Google</span>
                  </>
                )}
              </button>

              {/* Email Sign Up Button */}
              <button
                type="button"
                onClick={() => handleModeChange('email-signup')}
                className={cn(
                  'w-full flex items-center justify-center gap-3 px-6 py-3',
                  'bg-white/10 hover:bg-white/20 text-white font-medium rounded-full',
                  'transition-all duration-200 focus:outline-none focus:ring-2 focus:ring-white/50',
                  'border border-white/30'
                )}
              >
                <Mail className="w-5 h-5" />
                <span className="text-[15px]">Sign up with email</span>
              </button>
            </div>

            {/* Error Message */}
            {message && (
              <div
                className={cn(
                  'w-full p-3 rounded-lg text-sm mb-6',
                  message.type === 'success'
                    ? 'bg-green-500/20 text-green-200 border border-green-500/30'
                    : 'bg-red-500/20 text-red-200 border border-red-500/30'
                )}
              >
                {message.text}
              </div>
            )}

            {/* Already have account */}
            <p className="text-white/70 text-center text-sm mb-8">
              Already have an account?{' '}
              <button
                type="button"
                onClick={() => handleModeChange('email-signin')}
                className="text-white hover:text-white/90 font-medium transition-colors underline"
              >
                Sign in
              </button>
            </p>

            {/* Footer Links */}
            <div className="text-center space-x-3 text-xs text-white/50">
              <button
                type="button"
                onClick={() => setShowPrivacyModal(true)}
                className="hover:text-white/70 transition-colors underline"
              >
                Datenschutzerklärung
              </button>
              <span>·</span>
              <button
                type="button"
                onClick={() => setShowTermsModal(true)}
                className="hover:text-white/70 transition-colors underline"
              >
                Nutzungsbedingungen
              </button>
            </div>
          </>
        )}

        {/* Email Sign Up Screen */}
        {mode === 'email-signup' && (
          <>
            <button
              type="button"
              onClick={() => handleModeChange('welcome')}
              className="self-start mb-8 flex items-center gap-2 text-white/70 hover:text-white transition-colors"
            >
              <ArrowLeft className="w-5 h-5" />
              <span>Back</span>
            </button>

            <h2 className="text-3xl font-bold text-white mb-8 text-center self-stretch">
              Create your account
            </h2>

            <form onSubmit={handleEmailSignUp} className="w-full space-y-4">
              <Input
                type="email"
                label="Email address"
                placeholder="you@example.com"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                required
                autoComplete="email"
              />

              <Input
                type="password"
                label="Password"
                placeholder="At least 6 characters"
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                required
                autoComplete="new-password"
              />

              {message && (
                <div
                  className={cn(
                    'p-3 rounded-lg text-sm',
                    message.type === 'success'
                      ? 'bg-green-500/20 text-green-200 border border-green-500/30'
                      : 'bg-red-500/20 text-red-200 border border-red-500/30'
                  )}
                >
                  {message.text}
                </div>
              )}

              <Button
                type="submit"
                className="w-full"
                isLoading={isSubmitting}
                disabled={!email || !password}
              >
                Create Account
              </Button>

              <p className="text-xs text-center text-white/60 leading-relaxed pt-2">
                By signing up, you agree to our{' '}
                <button
                  type="button"
                  onClick={() => setShowPrivacyModal(true)}
                  className="text-white/80 underline hover:text-white transition-colors"
                >
                  Privacy Policy
                </button>
                {' '}and{' '}
                <button
                  type="button"
                  onClick={() => setShowTermsModal(true)}
                  className="text-white/80 underline hover:text-white transition-colors"
                >
                  Terms of Service
                </button>
              </p>
            </form>
          </>
        )}

        {/* Email Sign In Screen */}
        {mode === 'email-signin' && (
          <>
            <button
              type="button"
              onClick={() => handleModeChange('welcome')}
              className="self-start mb-8 flex items-center gap-2 text-white/70 hover:text-white transition-colors"
            >
              <ArrowLeft className="w-5 h-5" />
              <span>Back</span>
            </button>

            <h2 className="text-3xl font-bold text-white mb-8 text-center self-stretch">
              Welcome back
            </h2>

            <form onSubmit={handleEmailSignIn} className="w-full space-y-4">
              <Input
                type="email"
                label="Email address"
                placeholder="you@example.com"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                required
                autoComplete="email"
              />

              <Input
                type="password"
                label="Password"
                placeholder="Enter your password"
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                required
                autoComplete="current-password"
              />

              {message && (
                <div
                  className={cn(
                    'p-3 rounded-lg text-sm',
                    message.type === 'success'
                      ? 'bg-green-500/20 text-green-200 border border-green-500/30'
                      : 'bg-red-500/20 text-red-200 border border-red-500/30'
                  )}
                >
                  {message.text}
                </div>
              )}

              <Button
                type="submit"
                className="w-full"
                isLoading={isSubmitting}
                disabled={!email || !password}
              >
                Sign In
              </Button>

              <button
                type="button"
                onClick={() => handleModeChange('forgot')}
                className="w-full text-center text-sm text-white/60 hover:text-white transition-colors pt-2"
              >
                Forgot your password?
              </button>
            </form>
          </>
        )}

        {/* Forgot Password Screen */}
        {mode === 'forgot' && (
          <>
            <button
              type="button"
              onClick={() => handleModeChange('email-signin')}
              className="self-start mb-8 flex items-center gap-2 text-white/70 hover:text-white transition-colors"
            >
              <ArrowLeft className="w-5 h-5" />
              <span>Back</span>
            </button>

            <h2 className="text-3xl font-bold text-white mb-8 text-center self-stretch">
              Reset your password
            </h2>

            <form onSubmit={handleForgotPassword} className="w-full space-y-4">
              <Input
                type="email"
                label="Email address"
                placeholder="you@example.com"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                required
                autoComplete="email"
              />

              {message && (
                <div
                  className={cn(
                    'p-3 rounded-lg text-sm',
                    message.type === 'success'
                      ? 'bg-green-500/20 text-green-200 border border-green-500/30'
                      : 'bg-red-500/20 text-red-200 border border-red-500/30'
                  )}
                >
                  {message.text}
                </div>
              )}

              <Button
                type="submit"
                className="w-full"
                isLoading={isSubmitting}
                disabled={!email}
              >
                Send Reset Link
              </Button>
            </form>
          </>
        )}
      </div>

      {/* Modals */}
      <PrivacyPolicyModal
        isOpen={showPrivacyModal}
        onClose={() => setShowPrivacyModal(false)}
      />
      <TermsOfServiceModal
        isOpen={showTermsModal}
        onClose={() => setShowTermsModal(false)}
      />
    </>
  );
}
